package com.hanif.smartstudy.focus

// ═══════════════════════════════════════════════════════════════════
//  FocusModeConfig — ফোকাস মোড ফিচারের একমাত্র "মাস্টার সুইচ"
// ───────────────────────────────────────────────────────────────────
//  এই ফাইলটাই ফোকাস মোডের পুরো ফিচারের কেন্দ্রীয় কনফিগ।
//  ENABLED = false করে দিলে warning screen, নাজ (nudge), background
//  notification — সব সাথে সাথে বন্ধ হয়ে যাবে, বাকি অ্যাপ সম্পূর্ণ
//  স্বাভাবিকভাবে চলবে (কোনো ক্র্যাশ/সাইড-ইফেক্ট ছাড়াই)।
//
//  এটা ছাড়াও safety-net এর জন্য কয়েকটা constant এখানেই রাখা হলো,
//  যাতে ভবিষ্যতে সীমা বদলাতে হলে একটাই জায়গায় বদলালেই হয়।
// ═══════════════════════════════════════════════════════════════════
object FocusModeConfig {

    /** মাস্টার সুইচ — পুরো ফোকাস মোড ফিচার চালু/বন্ধ করার একমাত্র জায়গা */
    const val ENABLED: Boolean = true

    /** ফোকাস মোড এখন সবার জন্য উন্মুক্ত — আগে শুধু Admin-দের জন্য ছিল। */
    const val ADMIN_ONLY: Boolean = false

    /** পরীক্ষার তারিখ না পার হলেও, ভুলে বন্ধ না করলে সর্বোচ্চ এই কয়দিন পর
     *  ফোকাস মোড নিজে থেকেই বন্ধ হয়ে যাবে (hard cap safety net) */
    const val MAX_ACTIVE_DAYS: Int = 7

    /** ব্যাকগ্রাউন্ডে থাকা অবস্থায় কত মিনিট পরপর reminder notification যাবে
     *  (Part ৪ এ ব্যবহৃত হবে — এখনই সংযুক্ত করা হয়নি) */
    const val REMINDER_INTERVAL_MINUTES: Int = 10
}
